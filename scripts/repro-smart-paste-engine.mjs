/**
 * Phase 1 smoke: CleaningEngine + HeaderDetection + RuleMapping (no DOM).
 * Run: node ./scripts/repro-smart-paste-engine.mjs
 */
import { pathToFileURL } from "node:url";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const engine = path.join(__dirname, "../src/pages/datacapture/paste/engine");

const { CleaningEngine } = await import(
  pathToFileURL(path.join(engine, "CleaningEngine.js")).href,
);
const { HeaderDetectionEngine } = await import(
  pathToFileURL(path.join(engine, "HeaderDetectionEngine.js")).href,
);
const { RuleMappingEngine } = await import(
  pathToFileURL(path.join(engine, "RuleMappingEngine.js")).href,
);
const { ConfidenceEngine } = await import(
  pathToFileURL(path.join(engine, "ConfidenceEngine.js")).href,
);
const { ExcelTsvParser } = await import(
  pathToFileURL(path.join(engine, "plugins/ExcelTsvParser.js")).href,
);

let failed = 0;

function assert(name, cond, detail = "") {
  if (cond) {
    console.log(`PASS ${name}`);
  } else {
    console.log(`FAIL ${name}${detail ? `: ${detail}` : ""}`);
    failed += 1;
  }
}

const overselectPlain = {
  headers: [],
  rows: [
    ["Monthly Report"],
    ["Generated By System"],
    ["Invoice No", "Amount"],
    ["INV001", "100"],
    ["INV002", "200"],
    ["Page 1 of 1"],
  ],
  meta: {},
};

const cleaner = new CleaningEngine();
const cleaned = cleaner.clean(overselectPlain);
assert(
  "clean-drops-chrome",
  cleaned.rows.length >= 2 &&
    cleaned.rows.some((r) => r[0] === "INV001" || r.includes("INV001")),
  JSON.stringify(cleaned.rows),
);
assert(
  "clean-no-page-footer",
  !cleaned.rows.some((r) => /page\s+1\s+of\s+1/i.test(r.join(" "))),
  JSON.stringify(cleaned.rows),
);

const headerEngine = new HeaderDetectionEngine();
const detected = headerEngine.detect(cleaned);
assert("header-detected-or-data", detected.rows.length >= 1, JSON.stringify(detected));

const tsv = `Doc Ref\tPosting Date\tNett Amount\nINV001\t2024-01-15\t100.00\nINV002\t2024-01-16\t200.50`;
assert("tsv-canParse", ExcelTsvParser.canParse({ plain: tsv }) >= 0.35);
const parsed = ExcelTsvParser.parse({ plain: tsv });
assert("tsv-rows", parsed?.rows?.length === 3, JSON.stringify(parsed?.rows));

const mapper = new RuleMappingEngine();
const mapping = mapper.map(parsed.rows[0], parsed.rows.slice(1));
assert(
  "rule-maps-doc",
  mapping.mappings.some((m) => m.field === "document_no" && m.confidence >= 70),
  JSON.stringify(mapping.mappings),
);
assert(
  "rule-maps-amount",
  mapping.mappings.some((m) => m.field === "amount" && m.confidence >= 70),
  JSON.stringify(mapping.mappings),
);

const conf = new ConfidenceEngine().evaluate(mapping);
assert("confidence-numeric", conf.score >= 0 && conf.score <= 100, JSON.stringify(conf));

console.log(failed ? `\n${failed} failed` : "\nAll engine smoke checks passed");
process.exit(failed ? 1 : 0);
